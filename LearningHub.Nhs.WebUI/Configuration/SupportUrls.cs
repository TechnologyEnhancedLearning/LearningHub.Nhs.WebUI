namespace LearningHub.Nhs.WebUI.Configuration
{
    /// <summary>
    /// Defines the <see cref="SupportUrls" />.
    /// </summary>
    public class SupportUrls
    {
        /// <summary>
        /// Gets or sets the SupportSite.
        /// </summary>
        public string SupportSite { get; set; }

        /// <summary>
        /// Gets or sets the SupportForm.
        /// </summary>
        public string SupportForm { get; set; }

        /// <summary>
        /// Gets or sets the Bookmarks.
        /// </summary>
        public string BookmarksHelpUrl { get; set; }

        /// <summary>
        /// Gets or sets the SupportFeedbackForm.
        /// </summary>
        public string SupportFeedbackForm { get; set; }

        /// <summary>
        /// Gets or sets the ExcludedFiles.
        /// </summary>
        public string ExcludedFiles { get; set; }

        /// <summary>
        /// Gets or sets the WhoCanAccessTheLearningHub.
        /// </summary>
        public string WhoCanAccessTheLearningHub { get; set; }

        /// <summary>
        /// Gets or sets the ContributeUR.
        /// </summary>
        public string ContributeUR { get; set; }

        /// <summary>
        /// Gets or sets the PrivacyNotice.
        /// </summary>
        public string PrivacyNotice { get; set; }

        /// <summary>
        /// Gets or sets the UnableToViewELearningResources.
        /// </summary>
        public string UnableToViewELearningResources { get; set; }

        /// <summary>
        /// Gets or sets the contributing a resource url.
        /// </summary>
        public string ContributingAResource { get; set; }

        /// <summary>
        /// Gets or sets the report a resource url.
        /// </summary>
        public string ReportAResource { get; set; }

        /// <summary>
        /// Gets or sets the user account upgrade url.
        /// </summary>
        public string UserAccountUpgrade { get; set; }

        /// <summary>
        /// Gets or sets the my learning help url.
        /// </summary>
        public string MyLearningHelpUrl { get; set; }

        /// <summary>
        /// Gets or sets the validation errors help url.
        /// </summary>
        public string ValidationErrorsHelpUrl { get; set; }

        /// <summary>
        /// Gets or sets the resource certificate support url.
        /// </summary>
        public string ResourceCertificateUrl { get; set; }
    }
}
