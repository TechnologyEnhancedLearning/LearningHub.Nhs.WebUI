namespace LearningHub.Nhs.OpenApi.Repositories.Interface.Repositories.Resources
{
    using System.Threading.Tasks;
    using LearningHub.Nhs.Models.Entities.Resource;

    /// <summary>
    /// The GenericFileResourceVersionRepository interface.
    /// </summary>
    public interface IHtmlResourceVersionRepository : IGenericRepository<HtmlResourceVersion>
    {
        /// <summary>
        /// The get by id async.
        /// </summary>
        /// <param name="id">The id.</param>
        /// <returns>The <see cref="Task"/>.</returns>
        Task<HtmlResourceVersion> GetByIdAsync(int id);

        /// <summary>
        /// The get by resource version id async.
        /// </summary>
        /// <param name="id">The id.</param>
        /// <param name="includeDeleted">Allows deleted items to be returned.</param>
        /// <returns>The <see cref="Task"/>.</returns>
        Task<HtmlResourceVersion> GetByResourceVersionIdAsync(int id, bool includeDeleted = false);

        /// <summary>
        /// The get by resource version id.
        /// </summary>
        /// <param name="id">The id.</param>
        /// <param name="includeDeleted">Allows deleted items to be returned.</param>
        /// <returns>The html resource version.</returns>
        HtmlResourceVersion GetByResourceVersionId(int id, bool includeDeleted = false);
    }
}
